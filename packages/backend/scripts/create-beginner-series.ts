async function createArticles() {
  try {
    console.log('Creating article series...');

    const { data: authUsers, error: authError } = await supabase.auth.admin.listUsers();

    if (authError || !authUsers || authUsers.users.length === 0) {
      console.error('Auth error:', authError);
      throw new Error('No users found. Please create a user first.');
    }

    const authorId = authUsers.users[0].id;
    console.log('Author ID: ' + authorId);

    let previousArticleId: string | null = null;

    for (let i = 0; i < articles.length; i++) {
      const article = articles[i];

      console.log('Creating: ' + article.title);

      const { data: newArticle, error: createError } = await supabase
        .from('articles')
        .insert({
          title: article.title,
          content: article.content,
          author_id: authorId,
          is_published: true,
          published_at: new Date().toISOString(),
        })
        .select()
        .single();

      if (createError || !newArticle) {
        console.error('Create error:', createError);
        throw createError;
      }

      console.log('Created: ' + newArticle.id);

      if (previousArticleId) {
        const { error: updateError } = await supabase
          .from('articles')
          .update({ next_article_id: newArticle.id })
          .eq('id', previousArticleId);

        if (updateError) {
          console.error('Update error:', updateError);
        } else {
          console.log('Linked to previous article');
        }
      }

      previousArticleId = newArticle.id;
    }

    console.log('All articles created successfully!');
    console.log('Total articles: ' + articles.length);
  } catch (error) {
    console.error('Error:', error);
    process.exit(1);
  }
}

